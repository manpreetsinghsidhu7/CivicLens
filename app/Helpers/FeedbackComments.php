     * Get random comments for a given category
     */
    public static function getComments(string $category, int $count = 10): array
    {
        $cat = strtolower(trim($category));
        $pool = self::$comments[$cat] ?? [];

        // Merge with general comments for variety
        $pool = array_merge($pool, self::$comments['general']);

        shuffle($pool);
        return array_slice($pool, 0, min($count, count($pool)));
    }

